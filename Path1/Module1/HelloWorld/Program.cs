Console.WriteLine("This is a string");
Console.Write("This is below the first string");
Console.Write("This is appended to the previous text.");
//Console - Class, WriteLine() - Method. Write() - Another method within the Console class.

//Character Literals
Console.WriteLine('b');

//Integer Literals
Console.WriteLine(100);

//Float Literal
Console.WriteLine(0.25F);

//Double Literal (Compiler Default)
Console.WriteLine(2.625);

//Decimal Literal
Console.WriteLine(12.39816m);

//Boolean Literals
Console.WriteLine(true);
Console.WriteLine(false);

//Declaring Variables
string name;
int number;
double first;

//Initializing Variables
name = "John";
number = 0;
first = 10;

//Getting Variable Values
Console.WriteLine(name);
Console.WriteLine(number);
Console.WriteLine(first);
